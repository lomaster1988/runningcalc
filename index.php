$sec_result = $time - ($min_rounded * $distance_km * 60); // считаем секунды при таком темпе в минетах (получили  2970) и вычитаем это число из общего кол-ва секунд

$sec_result_2 = $sec_result / $distance_km; // вычислили секунды

$sec_result_rounded = round($sec_result_2); // округляем это число "обычным" round, погрешность составит до 0.5 секунды

$speed = 3600 / $pace;

$speed_result_rounded = round ($speed, 1);




